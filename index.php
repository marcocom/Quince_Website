function getIdFromUrl($url)
{
    $url = preg_replace('/^https:\/\/www.facebook.com/', '', $url);
    $pos = strrpos($url, '/');
    if ($pos === false)
    {
        return false;
    }

    $id = trim(substr($url, $pos));
    if(stripos($id,'?') > 0){
        $id = trim(substr($id,0,stripos($id,'?')));
    }
    if ( empty($id) )
    {
        return false;
    }



    return $id;
}
